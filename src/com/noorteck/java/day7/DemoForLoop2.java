public class DemoForLoop2 {

	
	
	public static void main(String args[]){
		
	for(int i=20 ; i<=50 ; i++) {
			
			
			System.out.println(i);
		}
			
		System.out.println("****************************");
		
		
for(int i=40 ; i>=30 ; i--) {
			
			
			System.out.println(i);
		}
		
		
		
		
		
		
	}
}
