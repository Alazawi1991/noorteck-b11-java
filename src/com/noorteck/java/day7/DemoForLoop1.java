package com.noorteck.java.day7;

public class DemoForLoop1 {
	
	
	
	public static void main(String args []) {
		
		
		
		for(int i=1 ;i<=5; i++) {
			
			System.out.println(i);
			
		}
		
		/**
		Syntax For Loop:
		
			for (Initialization; boolean_expression; update) {
			
			
			}

			Initialization -> is where we declare a variable and assign value
						  
			boolean_expression --> control whether it should go inside the loop body or not
			
						if the boolean_expression is TRUE --> then control goes to the loop body
						if the boolean_expression is FALSE --> then it will not go inside the loop body
						
			update --> increment/decrement


	*/
		
		
	}
	

}
