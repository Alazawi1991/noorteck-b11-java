package com.noorteck.java.day28;

public class Person {
	private String name;
	private int age;
	private char gender;

	public void setname(String name) {
		this.name = name;

	}

	public String getname() {
		return name;
	}

	public void setage(int age) {
		this.age = age;
	}

	public int getage() {
		return age;
	}

	public void setgender(char gender) {

		this.gender = gender;
	}

	public char getgender() {

		return gender;
	}

}