package com.noorteck.java.day8;

public class DemoDoWhile2 {

	public static void main(String args[]){

		for (int i = 5; i <= 0; i++) {
			System.out.println(i);
		}

		System.out.println("**********");

		int j = 5;
		while (j <= 0) {

			System.out.println(j);
		}
		System.out.println("****************");
		
		int y=5;
				
				do {
				
					System.out.println(y);
					y++;}
					while (y<=0);
				
				System.out.println("***********");
		

	}

}
